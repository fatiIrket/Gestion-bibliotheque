    private void AjouterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AjouterMouseClicked
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/gestion_biblio", "root", "");
            String query = "INSERT INTO `etudiant`(`id`, `nom`,`prenom`,`email`,`psw`,date_naiss) VALUES (?,?,?,?,?,?)";
            pst = con.prepareStatement(query);
            pst.setString(1, this.id_etud.getText());
            pst.setString(2, this.TNomm.getText());
            pst.setString(3, this.Tprenom.getText());
            pst.setString(4, this.Tadresse.getText());
            pst.setString(5, this.date_naiss.getText());
            pst.setString(6, this.psw.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "etudiant AjoutÃ©");
        } catch (Exception ex) {
            JOptionPane.showInputDialog(ex);
        }
        this.Tadresse.setText("");
        this.id_etud.setText("");
         this.Tprenom.setText("");
         this.TNomm.setText("");
         this.psw.setText("");
         this.date_naiss.setText("");
    }//GEN-LAST:event_AjouterMouseClicked

